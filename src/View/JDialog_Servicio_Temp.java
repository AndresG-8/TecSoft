    private boolean guardar() {
        boolean rpta = false;
        try {
            modelo = (DefaultTableModel) jTable1.getModel();
            int rowcount = jTable1.getRowCount();
            for(int i = 0; i < rowcount; i++){
                servicio_temp temp = new servicio_temp();
                temp.setPlaca(jTable1.getValueAt(i, 0).toString());
                temp.setNom_ser_temp(jTable1.getValueAt(i, 1).toString());
                temp.setProcedimiento(jTable1.getValueAt(i, 2).toString());
                temp.setCobro(Double.parseDouble(jTable1.getValueAt(i, 3).toString()));

                if(con.guardar_ser_tem(temp)){
                    JOptionPane.showMessageDialog(this, "Fila " + i + " guardada");//QUUITAR ESTE MENSAJE. SOLO POR VERIFICAR   
                    jButton2.setEnabled(false);
                    rpta = true;
                }else{
                    JOptionPane.showMessageDialog(this,"Error al guardar datos");   
                    rpta = false;
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error guardando el servicio temporal\n" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE );
        }
        return rpta;
    }
