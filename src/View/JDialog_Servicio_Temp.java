    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!"".equals(txtPlaca.getText()) &&!"".equals(txtNombre.getText()) && !"".equals(txtCobro.getText())){
            modelo = (DefaultTableModel) jTable1.getModel();
            Object[] fila = new Object[4];
            fila[0] = txtPlaca.getText();
            fila[1] = txtNombre.getText();
            fila[2] = jTextArea1.getText();
            fila[3] = txtCobro.getText();
            cobro += Float.parseFloat(txtCobro.getText());
            txtTotal_Cobro.setText(""+cobro);
            modelo.addRow(fila);
            jTable1.setModel(modelo);
            
            //luego por cada dato guardado, se almacena temporalmente un properties. 
            //LA CONTRA ES QUE NO SE PUEDEN GUARDAR VARIOS PROPERTIES DE SERVICIOS :C
            //txtPlaca.setText("");
            txtNombre.setText("");
            jTextArea1.setText("");
            txtCobro.setText("");
            jButton2.setEnabled(true);
        }else{
            JOptionPane.showMessageDialog(this, "Se requiere llenar minimo la Placa, el Nombre y el Cobro del servicio");
        }       
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        modelo = (DefaultTableModel) jTable1.getModel();
        int a = jTable1.getSelectedRow();
        if(a < 0){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila de la tabla");
        }else{
            int confirmar = JOptionPane.showConfirmDialog(null, "Esta seguro que desea eliminar el registro");
            if(JOptionPane.OK_OPTION == confirmar){
                float resta = Float.parseFloat(jTable1.getValueAt(a, 3).toString());
                cobro -= resta;
                txtTotal_Cobro.setText(""+cobro);
                modelo.removeRow(a);
                JOptionPane.showMessageDialog(this, "Registro elimiando");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        modelo = (DefaultTableModel) jTable1.getModel();
        int fil_sel = jTable1.getSelectedRow();
        if(fil_sel == -1){
            JOptionPane.showMessageDialog(this, "Seleccione de la lista un servicio para modificarlo");
        }else{
            txtPlaca.setText(jTable1.getValueAt(fil_sel, 0).toString());
            txtNombre.setText(jTable1.getValueAt(fil_sel, 1).toString());
            jTextArea1.setText(jTable1.getValueAt(fil_sel, 2).toString());
            txtCobro.setText(jTable1.getValueAt(fil_sel, 3).toString());
            float resta = Float.parseFloat(jTable1.getValueAt(fil_sel, 3).toString());
            cobro -= resta;
            txtTotal_Cobro.setText(""+cobro);
            modelo.removeRow(fil_sel);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

