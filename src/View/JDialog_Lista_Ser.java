    private void guardar() {
        lista_servicio ls = new lista_servicio();
        ls.setNombre_ser(txtNombre.getText());
        ls.setTipo_ser(txtTipo.getText());
        ls.setDiag_inicial(jTextArea1.getText());
        if(con.verificar_ser(ls) == false){
            if(con.guardar_ser(ls)){
                JOptionPane.showMessageDialog(this,"Datos Guardados");
                limpiar();
                //btnAgregar.setEnabled(false);
            }else{
                JOptionPane.showMessageDialog(this,"Error al guardar datos");   
            } 
        }    
        else{
            JOptionPane.showMessageDialog(this,"El servicio de tipo: "+ ls.getTipo_ser() + " ya existe\nCodigo: " + ls.getCod_ser()+ "\nNombre: " + ls.getNombre_ser());
        } 
    }

    private void cargar() {
        con.cargar("SELECT * FROM lista_servicio", modelo);
    }
    
    private boolean modificar_lista() {
        boolean rpta = false;
        lista_servicio ls = new lista_servicio();
        ls.setCod_ser(Integer.parseInt(jLabel4.getText()));
        ls.setNombre_ser(txtNombre.getText());
        ls.setTipo_ser(txtTipo.getText());
        ls.setDiag_inicial(jTextArea1.getText());
        if(con.modificar_lista_ser(ls)){
                JOptionPane.showMessageDialog(this,"Datos modificados con exito");
                limpiar();
                jLabel4.setText("");
                jLabel5.setText("");
                rpta = true;
                //btnAgregar.setEnabled(false);
        }else{
                JOptionPane.showMessageDialog(this,"Error al modificar los datos");   
        } 
        return rpta;
    }
 
    public boolean eliminar_lista(int cod){
        boolean rpta = false;
            lista_servicio ls = new lista_servicio();
            ls.setCod_ser(cod);
            if(con.eliminar_lista_ser(ls)){
               rpta = true; 
            }else{
                JOptionPane.showMessageDialog(null, "Error eliminando el cliente");
            }
        
        return rpta;
    }
    
    private void limpiar() {
        txtNombre.setText("");
        txtTipo.setText("");
        jTextArea1.setText("");
    }
    
    
    private void formato_ancho(){
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(60);
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(107);
        jTable1.getColumnModel().getColumn(3).setPreferredWidth(360);
    }

   
