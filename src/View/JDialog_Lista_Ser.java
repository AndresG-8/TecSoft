    private void btnModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModActionPerformed
        // modificar
        String texto = btnMod.getText();
        switch(texto){
            case "Modificar":
                modelo = (DefaultTableModel) jTable1.getModel();
                int fil_sel = jTable1.getSelectedRow();
                if(fil_sel < 0){
                    JOptionPane.showMessageDialog(this, "Debe seleccionar un elemento");
                }else{
                    jLabel5.setText("Codigo:");
                    jLabel4.setText(jTable1.getValueAt(fil_sel, 0).toString());
                    txtNombre.setText(jTable1.getValueAt(fil_sel, 1).toString());
                    txtTipo.setText(jTable1.getValueAt(fil_sel, 2).toString());
                    jTextArea1.setText(jTable1.getValueAt(fil_sel, 3).toString());
                    btnMod.setText("Guardar");
                    btnEliminar.setText("Cancelar");
                    //modelo.removeRow(fil_sel);//en ves de removerla, que haga 
                }
            break;
            case "Guardar":
                if(!"".equals(txtNombre.getText()) && !"".equals(txtTipo.getText()) && !"".equals(jTextArea1.getText())){
                    if(con.getCon() != null){
                        if(modificar_lista()){
                            cargar();
                            formato_ancho();
                            btnMod.setText("Modificar");
                            btnEliminar.setText("Eliminar");
                        }
                    }else{
                        JOptionPane.showMessageDialog(this, "La conexi√≥n esta cerrada");
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "¬°Todos los campos son obligatorios!");
                }
            break;
        }
    }//GEN-LAST:event_btnModActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String el = btnEliminar.getText();
        switch(el){
            case "Eliminar":
                modelo = (DefaultTableModel) jTable1.getModel();
                int fil_sel = jTable1.getSelectedRow();
                if(fil_sel < 0){
                    JOptionPane.showMessageDialog(this, "Debe seleccionar un elemento");
                }else{
                    if(JOptionPane.showConfirmDialog(this, "¬øEsta seguro que desea eliminar el registro?", 
                        "Eliminar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == 0){
                        if(con.getCon() != null){
                            int cod = Integer.parseInt((jTable1.getValueAt(fil_sel, 0).toString()));
                            if(eliminar_lista(cod)){
                                JOptionPane.showMessageDialog(this, "Registro eliminado con exito");
                                cargar();
                                formato_ancho();
                            }
                            }else{
                                JOptionPane.showMessageDialog(this, "La conexi√≥n esta cerrada");
                        }
                    }
                }
            break;
            case "Cancelar":
                limpiar();
                jLabel4.setText("");
                jLabel5.setText("");
                btnMod.setText("Modificar");
                btnEliminar.setText("Eliminar");
            break;
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

