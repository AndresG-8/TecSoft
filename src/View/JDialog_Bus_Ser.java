    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int fil_sel = jTable1.getSelectedRow();
        if(fil_sel < 0){
            JOptionPane.showMessageDialog(this, "Debe seleccionar un servicio");
        }else{
            JFrame_Servicio.txtCod_Ser.setText(jTable1.getValueAt(fil_sel, 0).toString());
            JFrame_Servicio.txtNom_Ser.setText(jTable1.getValueAt(fil_sel, 1).toString());
            JFrame_Servicio.txtTipo.setText(jTable1.getValueAt(fil_sel, 2).toString());
            JFrame_Servicio.jTextArea2.setText(jTable1.getValueAt(fil_sel, 3).toString());
            this.dispose();
        }       
    }//GEN-LAST:event_jButton1ActionPerformed

