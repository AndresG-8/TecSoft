    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        switch(pariente){
            case "Nuevo Cliente":
                int fil_sel = jTable1.getSelectedRow();
                String a;
                if(fil_sel == - 1){
                    JOptionPane.showMessageDialog(this, "Seleccione una Empresa");
                }else{
                    a = (jTable1.getValueAt(fil_sel, 0)).toString();
                    JDialog_Cliente.nit = a;
                    this.dispose();
                }
            break;
            case "Datos del Cliente":
                int fil_sel2 = jTable1.getSelectedRow();
                String a2;
                if(fil_sel2 == - 1){
                    JOptionPane.showMessageDialog(this, "Seleccione una Empresa");
                }else{
                    a2 = (jTable1.getValueAt(fil_sel2, 0)).toString();
                    JDialog_Cliente_General.nit_e = a2;
                    this.dispose();
                }
            break;
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

