    public void crear(){
         // creando el nuevo usuario
        if(con.getCon() != null){
            if(JOptionPane.showConfirmDialog(this, "¿Esta seguro que desea crear este NUEVO usuario?", "Crear", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == 0){
                String psw = new String (txtJPass.getPassword());
                String user = txtUser.getText();
                String pass = EncriptadoMD5.encriptaEnMD5(psw);
                Logon log = new Logon();
                log.setUser(user);
                log.setPass(pass);
                if(con.existe_user(log)){//si el usuario existe, que no lo guarde.
                    JOptionPane.showMessageDialog(this, "El usuario " + user + " ya existe. Intente con uno diferente");
                    vaciar();
                }else{
                    if(con.crear_user(log)){
                        JOptionPane.showMessageDialog(this, "Datos de nuevo Usuario guardados con exito"); 
                        vaciar();
                    }else{
                        JOptionPane.showMessageDialog(this, "Error intentando guardar el nuevo Usuario ");
                        vaciar();
                    }
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "La conexión a la base de datos esta cerrada");
        }
    }

    private boolean isPasswordCorrect(char[] j1, char[] j2){
        boolean valor = true;
        int puntero = 0;
        if(j1.length != j2.length){//axamina que el tamaño sea igual
            valor = false;
        }else{
            while((valor) && (puntero < j1.length)){
                if(j1[puntero] != j2[puntero]){//verifica uno a uno que los caracteres ingresados sean los mismos
                    valor = false;//si uno es diferente, causa error
                }
                puntero++;
            }
        }
        return valor;    
    }

    private void vaciar() {
        txtUser.setText("");
        txtJPass.setText("");
        txtJPass1.setText("");
        txtUser.requestFocus();
    }
