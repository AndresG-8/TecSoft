    private void CrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearActionPerformed
        // creando el nuevo usuario
        if(!"".equals(txtUser.getText()) && !"".equals(txtJPass.getPassword()) && !"".equals(txtJPass1.getPassword())){
            if(isPasswordCorrect(txtJPass.getPassword(), txtJPass1.getPassword())){
                lblPass.setForeground(Color.black);
                lblPass.setText("Las contraseñas coinciden");
                crear();
                lblPass.setText("");
            }else{
                lblPass.setForeground(Color.red);
                lblPass.setText("Las contraseñas NO coinciden");
                txtJPass.requestFocus();
                txtJPass.setText("");
                txtJPass1.setText("");
            }
        }else{
            JOptionPane.showMessageDialog(this, "¡Todos los campos son obligatorios!");
        }
    }//GEN-LAST:event_CrearActionPerformed

