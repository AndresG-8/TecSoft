    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // si el parent es home haga esto
        switch (pariente) {
            case "Home":
                this.dispose();
                break;
            case "Servicio":
                //que pregunte por la fila seleccionada
                int fil_sel = jTable1.getSelectedRow();
                if(fil_sel < 0){
                    JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente");
                }else{
                  //cuando tenga le cliente sleeccionado, debere cargar los datos al objeto bus_cli
                    //luego de alli se recuperaran los valores y se le psara al jframe_servicio
                    int columncount = jTable1.getColumnCount();
                    if(columncount <= 4){
                        JFrame_Servicio.jLabel7.setText(jTable1.getValueAt(fil_sel, 0).toString());
                        JFrame_Servicio.jLabel6.setText(jTable1.getValueAt(fil_sel, 1).toString());
                        JFrame_Servicio.jLabel10.setText(jTable1.getValueAt(fil_sel, 2).toString());
                        JFrame_Servicio.txtPlaca.setText(jTable1.getValueAt(fil_sel, 3).toString());
                        JFrame_Servicio.jLabel1.setText("");
                        JFrame_Servicio.jLabel9.setText(""); 
                        this.dispose();
                    }
                    if(columncount > 4){
                        JFrame_Servicio.jLabel7.setText(jTable1.getValueAt(fil_sel, 0).toString());
                        JFrame_Servicio.jLabel6.setText(jTable1.getValueAt(fil_sel, 1).toString());
                        JFrame_Servicio.jLabel10.setText(jTable1.getValueAt(fil_sel, 2).toString());
                        JFrame_Servicio.txtPlaca.setText(jTable1.getValueAt(fil_sel, 3).toString());
                        JFrame_Servicio.jLabel1.setText(jTable1.getValueAt(fil_sel, 4).toString());
                        JFrame_Servicio.jLabel9.setText(jTable1.getValueAt(fil_sel, 5).toString()); 
                        this.dispose();
                    }
                }
                break;
            case "Agenda":
                int fil_sel2 = jTable1.getSelectedRow();
                if(fil_sel2 < 0){
                    JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente");
                }else{
                    JDialog_Agenda.txtCliente.setText(jTable1.getValueAt(fil_sel2, 3).toString());
                    JDialog_Agenda.txtNombre.setText(jTable1.getValueAt(fil_sel2, 1).toString());
                    this.dispose();
                }
                break;
            case "Aceite":
                int fil_sel3 = jTable1.getSelectedRow();
                if(fil_sel3 < 0){
                    JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente");
                }else{
                    JDialog_Aceite.txtCliente.setText(jTable1.getValueAt(fil_sel3, 3).toString());
                    JDialog_Aceite.txtNombre.setText(jTable1.getValueAt(fil_sel3, 1).toString());
                    this.dispose();
                }
            break;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

